<template>
  <div class="login_area">
    <div style="height: 20%"></div>
    <div class="login_box">
      <div class="avatar_box">
        <h3>特教资源库后台管理</h3>
      </div>

      <div>
        <!--login form-->
        <el-form
          :model="form"
          :rules="rules"
          ref="form"
          label-width="0px"
          class="login-form"
        >
          <!-- user name -->
          <el-form-item prop="name">
            <el-input
              prefix-icon="el-icon-search"
              v-model="form.name"
              placeholder="请输入用户名、邮箱或者手机号"
            ></el-input>
          </el-form-item>

          <!-- password -->
          <el-form-item prop="password">
            <el-input
              prefix-icon="el-icon-search"
              type="password"
              v-model="form.password"
              placeholder="密码"
            ></el-input>
          </el-form-item>

          <!-- verify -->
          <el-form-item>
            <Verify
              :imgUrl ="validateImageUrl"
              :imgName="validateImageNames"
              :type="4"
              style="width: 100%"
              @success="success"
              :showButton="false"
              :barSize="{ width: '100%', height: '40px' }"
            ></Verify>
          </el-form-item>

          <!-- buttons -->
          <el-form-item class="btns">
            <el-button
              :disabled="form.isAble"
              type="primary"
              @click="submitForm('form')"
              >登录</el-button
            >
            <el-button :disabled="form.isAble" type="info">重置</el-button>
          </el-form-item>
        </el-form>
      </div>
    </div>
    <!--login form-->
  </div>
</template>

<script>
import Verify from "vue2-verify";
import { login } from "@/app/api/account";
import { loginModel } from "@/app/models/login.js";
import { Notification } from "element-ui";
import { mapMutations } from "vuex";
import * as types from "@/app/static/type";

export default {
  data() {
    return {
      validateImageUrl:"http://tjcdn.shec.edu.cn/",
      validateImageNames:["404.jpg"],
      form: loginModel,
      rules: {
        name: [
          { required: true, message: "请输入用户名、邮箱或者手机号", trigger: "blur" },
        ],
      },
    };
  },
  components: { Verify },
  methods: {
    success() {
      console.log("success!");
      this.form.isAble = false;
    },

    submitForm(formName) {
      let _this = this;
      this.$refs[formName].validate((valid) => {
        if (valid) {
          login({
            userName: _this.form.name,
            password: _this.form.password,
          }).then((res) => {
            console.log(res);
            if (res) {
              Notification.success({ message: "登录成功" });
              _this.$store.commit(
                types.APP + "/" + types.LOGIN,
                res.data.token
              );
              _this.$router.push("/admin/user/list");
            }
          });
        } else {
          console.log("error submit!!");
          return false;
        }
      });
    },
  },
};
</script>

